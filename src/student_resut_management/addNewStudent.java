    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
        String courseName = (String)jComboBox3.getSelectedItem();
        String branch = (String)jComboBox1.getSelectedItem();
        String rollnum = jTextField2.getText();
        String name = jTextField3.getText();
        String DOB = jTextField4.getText();
        String gender = (String)jComboBox2.getSelectedItem();
        String address = jTextField5.getText();
        String city = (String)jComboBox4.getSelectedItem();
        
        String []st = DOB.split("/");
        String dateOfBirth ="";
        for(int i=st.length-1;i>=0;i--){
            if(i > 0)
                dateOfBirth += st[i]+"-";
            else
                dateOfBirth += st[i];
        }
                
        try{
          Class.forName("com.mysql.cj.jdbc.Driver");
          Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentsResultManagement", "root","123456789root");
          
          Statement sta = con.createStatement();
          sta.executeUpdate("insert into students values('"+rollnum+"','"+name+"','"+courseName+"','"+branch+"','"+dateOfBirth+"','"+gender+"','"+address+"','"+city+"')");
          sta.executeUpdate("insert into studentsCredentials values ('"+rollnum+"','"+DOB+"')");
          JOptionPane.showMessageDialog(rootPane,"Data sucessfully added");
          setVisible(false);
          new addNewStudent().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane,e.toString());
        }
    }//GEN-LAST:event_SubmitActionPerformed

